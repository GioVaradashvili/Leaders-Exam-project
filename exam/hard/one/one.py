#  Find all unique triplets in an array that sum up to zero.
# Instructions:
# Input: A list of integers (e.g., [-1, 0, 1, 2, -1, -4]).
# Output: A list of unique triplets that sum to zero.

def three_sum(nums):
    nums.sort()
nums = [-1, 0, 1, 2, -1, -4]
result = three_sum(nums)
print(result)  